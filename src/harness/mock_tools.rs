fn ask_tool(
    args: Map<String, Value>,
    _ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let answer = args
        .get("options")
        .and_then(Value::as_array)
        .and_then(|options| options.first())
        .cloned()
        .unwrap_or_else(|| Value::String("mock answer".to_string()));

    Ok(ToolResult {
        status: ToolStatus::Pass,
        value: json!({
            "kind": "answer",
            "question": args.get("question").or_else(|| args.get("input")).cloned().unwrap_or(Value::Null),
            "answer": answer
        }),
        summary: "Mock answer selected".to_string(),
        warnings: vec![],
    })
}

