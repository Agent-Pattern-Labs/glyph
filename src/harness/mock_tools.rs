use serde_json::{Map, Value, json};

use super::tool_registry::ToolRegistry;
use super::types::{ToolResult, ToolStatus};
use crate::runtime::context::RuntimeContext;
use crate::runtime::errors::GlyphRuntimeError;

pub fn create_mock_tool_registry() -> ToolRegistry {
    let mut registry = ToolRegistry::new();
    registry
        .register("SPEC", spec_tool)
        .register("PLAN", plan_tool)
        .register("GEN", gen_tool)
        .register("CHECK", check_tool)
        .register("FIX", fix_tool)
        .register("PATCH", patch_tool)
        .register("SUM", summarize_tool)
        .register("SUMMARIZE", summarize_tool)
        .register("ASK", ask_tool)
        .register("EXPORT", export_tool)
        .register("RUN", run_tool)
        .register("READ", read_tool)
        .register("WRITE", write_tool);
    registry
}

fn spec_tool(
    args: Map<String, Value>,
    _ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    Ok(ToolResult {
        status: ToolStatus::Pass,
        value: json!({
            "kind": "spec",
            "normalized": true,
            "requirements": args
        }),
        summary: format!(
            "Specification captured ({} field{})",
            args.len(),
            if args.len() == 1 { "" } else { "s" }
        ),
        warnings: vec![],
    })
}

fn plan_tool(
    args: Map<String, Value>,
    _ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let input = args
        .get("input")
        .or_else(|| args.get("spec"))
        .cloned()
        .unwrap_or_else(|| Value::Object(args.clone()));
    Ok(ToolResult {
        status: ToolStatus::Pass,
        value: json!({
            "kind": "plan",
            "input": input,
            "steps": ["spec", "generate", "check", "repair", "export"]
        }),
        summary: "Structured plan created".to_string(),
        warnings: vec![],
    })
}

