fn read_tool(
    args: Map<String, Value>,
    ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let path = args
        .get("path")
        .or_else(|| args.get("input"))
        .and_then(Value::as_str)
        .unwrap_or("")
        .to_string();
    let value = ctx.read_file(&path).cloned().unwrap_or_else(|| {
        if path == "app" {
            json!({
                "kind": "file_bundle",
                "path": path,
                "files": [{
                    "path": "app/index.ts",
                    "content": "export const broken = true;\n"
                }],
                "issues": ["failing_tests"],
                "patches": 0
            })
        } else {
            json!({
                "kind": "file",
                "path": path,
                "content": ""
            })
        }
    });

    Ok(ToolResult {
        status: ToolStatus::Pass,
        value,
        summary: format!("Read {}", if path.is_empty() { "mock path" } else { &path }),
        warnings: vec![],
    })
}

fn write_tool(
    args: Map<String, Value>,
    ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let path = args
        .get("path")
        .or_else(|| args.get("input"))
        .and_then(Value::as_str)
        .unwrap_or("")
        .to_string();
    let content = args
        .get("content")
        .cloned()
        .unwrap_or(Value::String(String::new()));
    ctx.write_file(path.clone(), content.clone());

    Ok(ToolResult {
        status: ToolStatus::Pass,
        value: json!({
            "kind": "write_result",
            "path": path,
            "content": content,
            "written": true
        }),
        summary: format!(
            "Wrote {}",
            if path.is_empty() { "mock path" } else { &path }
        ),
        warnings: vec![],
    })
}

fn extract_issues(value: &Value) -> Vec<String> {
    value
        .get("issues")
        .and_then(Value::as_array)
        .map(|items| {
            items
                .iter()
                .filter_map(Value::as_str)
                .map(ToString::to_string)
                .collect()
        })
        .unwrap_or_default()
}

fn patch_value(target: Value, patch: Value) -> Value {
    match (target, patch) {
        (Value::Object(mut target), Value::Object(patch)) => {
            let patches = target.get("patches").and_then(Value::as_i64).unwrap_or(0) + 1;
            for (key, value) in patch {
                target.insert(key, value);
            }
            target.insert("patches".to_string(), Value::from(patches));
            Value::Object(target)
        }
        (target, Value::Object(mut patch)) => {
            patch.insert(
                "kind".to_string(),
                Value::String("patched_value".to_string()),
            );
            patch.insert("target".to_string(), target);
            patch.insert("patches".to_string(), Value::from(1));
            Value::Object(patch)
        }
        (target, _) => json!({
            "kind": "patched_value",
            "target": target,
            "patches": 1
        }),
    }
}

fn summarize_value(value: &Value) -> String {
    match value {
        Value::Null => "null".to_string(),
        Value::String(value) => value.clone(),
        Value::Number(value) => value.to_string(),
        Value::Bool(value) => value.to_string(),
        Value::Array(items) => format!("array with {} items", items.len()),
        Value::Object(object) => object
            .get("kind")
            .and_then(Value::as_str)
            .map(|kind| format!("{kind} object"))
            .unwrap_or_else(|| format!("object with {} keys", object.len())),
    }
}
