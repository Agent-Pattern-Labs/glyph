fn check_tool(
    args: Map<String, Value>,
    _ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let target = args
        .get("target")
        .or_else(|| args.get("input"))
        .cloned()
        .unwrap_or_else(|| Value::Object(args.clone()));
    let using = args
        .get("using")
        .and_then(Value::as_array)
        .cloned()
        .unwrap_or_default();
    let issues = extract_issues(&target);
    let status = if issues.is_empty() {
        if using.iter().any(|item| item == "risk") {
            ToolStatus::Warning
        } else {
            ToolStatus::Pass
        }
    } else {
        ToolStatus::Fail
    };
    let using_strings = using
        .iter()
        .filter_map(Value::as_str)
        .map(ToString::to_string)
        .collect::<Vec<_>>();

    Ok(ToolResult {
        status,
        value: json!({
            "kind": "check_report",
            "status": status.as_str(),
            "using": using,
            "issues": issues,
            "targetSummary": summarize_value(&target)
        }),
        summary: format!(
            "Check {}{}",
            status.as_str(),
            if using_strings.is_empty() {
                String::new()
            } else {
                format!(" via {}", using_strings.join(", "))
            }
        ),
        warnings: if status == ToolStatus::Fail {
            extract_issues(&target)
        } else {
            vec![]
        },
    })
}

fn fix_tool(
    args: Map<String, Value>,
    _ctx: &mut RuntimeContext,
) -> Result<ToolResult, GlyphRuntimeError> {
    let target = args
        .get("target")
        .or_else(|| args.get("input"))
        .cloned()
        .unwrap_or(Value::Null);
    let value = patch_value(
        target,
        json!({
            "fixed": true,
            "issues": [],
            "lastReport": args.get("report").cloned().unwrap_or(Value::Null),
            "max": args.get("max").cloned().unwrap_or(Value::Null)
        }),
    );

    Ok(ToolResult {
        status: ToolStatus::Pass,
        value,
        summary: "Mock fix applied".to_string(),
        warnings: vec![],
    })
}

